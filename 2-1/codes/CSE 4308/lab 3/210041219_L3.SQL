	ACCOUNT_NO CHAR(5),
	BALANCE NUMBER NOT NULL,
	CONSTRAINT AC PRIMARY KEY(ACCOUNT_NO)
);
CREATE TABLE CUSTOMER(
	CUSTOMER_NO CHAR(5),
	CUSTOMER_NAME VARCHAR2(20) NOT NULL,
	CUSTOMER_BRANCH VARCHAR2(10),
	CONSTRAINT C_PK PRIMARY KEY(CUSTOMER_NO)
);
